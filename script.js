function addTexts(){
    let keyCounter = 0;
    document.querySelectorAll('.texts__example').forEach(elem=>{
        let valueCounter = 0;
        elem.querySelectorAll('.exampleText').forEach((textBox)=>{
            textBox.querySelector('.exampleText__text').innerText = noDBTexts[Object.keys(noDBTexts)[keyCounter]][valueCounter];
            textBox.setAttribute('textNum', valueCounter);
            valueCounter+=1;
        });
        valueCounter = 0;
        keyCounter += 1;
    })
}

